#include <stdio.h>
/* This is a comment */

int main() {
/* comments next to the line will show the intented output */
  
  printf("Hello world\n"); /* Hello World */
  printf("Answer %d\n", 42); /* Answer 42 */
  printf("Name %s\n", "Ryan"); /* Name Ryan */
  printf("x %.1f i %d\n", 3.5, 100); /* x 3.5 i 100 */

}

